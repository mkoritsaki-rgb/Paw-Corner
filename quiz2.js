if (answer3==="Λάθος") score++;
let answer4=prompt("Οι σκύλοι δεν μπορούν να καταλάβουν τη διάθεση του ιδιοκτήτη τους. (Σωστό/Λάθος)");
if (answer4==="Λάθος") score++;
